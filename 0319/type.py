x = 123    #將x設定為整數int型態
print(type(x))

y = '123'  #將y設定為字串str型態
print(type(y))

z = 123.0  #將z設定為浮點數float型態
print(type(z))


x = 123+0j #將x設定為複數complex型態
print(type(x))

y = True   #將y設定為布林bool型態
print(type(y))

z=(123,456)#將x設定為序對tuple型態
print(type(z))


x = [123] #將x設定為清單list型態
print(type(x))

y = {123}   #將y設定為集合set型態
print(type(y))

z={1:123}#將x設定為字典dict型態
print(type(z))
